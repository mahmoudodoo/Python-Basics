
# Module 5: Data Structures - Example Script

# Lists
print("Lists Example:")
my_list = [1, 2, 3, 'Python', 'Code']
print(my_list)

# Adding elements to a list
my_list.append('New Element')
print("After adding an element:", my_list)

# Tuples
print("\nTuples Example:")
my_tuple = (1, 'Hello', 3.14)
print(my_tuple)

# Accessing tuple elements
print("First element of the tuple:", my_tuple[0])

# Dictionaries
print("\nDictionaries Example:")
my_dict = {'name': 'Alice', 'age': 30}
print(my_dict)

# Accessing dictionary values
print("Name:", my_dict['name'])

# Sets
print("\nSets Example:")
my_set = {1, 2, 3, 4, 5}
print(my_set)

# Adding elements to a set
my_set.add(6)
print("After adding an element:", my_set)

# Note: Lists are mutable, tuples are immutable, dictionaries are key-value pairs, and sets are unordered collections of unique elements.
